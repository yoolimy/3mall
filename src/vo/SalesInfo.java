package vo;

public class SalesInfo {
	private String each_month;
	private int sum_sales;
	
	public String getEach_month() {
		return each_month;
	}
	public void setEach_month(String each_month) {
		this.each_month = each_month;
	}
	public int getSum_sales() {
		return sum_sales;
	}
	public void setSum_sales(int sum_sales) {
		this.sum_sales = sum_sales;
	}
}
